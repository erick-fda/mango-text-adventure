========================================================================================*/
using UnityEngine;

/*========================================================================================
	DontDestroy
========================================================================================*/
public class DontDestroy : MonoBehaviour
{
	/*----------------------------------------------------------------------------------------
		MonoBehaviour Methods
	----------------------------------------------------------------------------------------*/
    private void Awake()
    {
        DontDestroyOnLoad(gameObject);
    }
}
